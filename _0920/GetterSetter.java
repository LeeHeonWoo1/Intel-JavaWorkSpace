package _0920;

public class GetterSetter {
    private int x;
    private int y;


    public void setX(int a){
        this.x = a;
    }

    public void setY(int a){
        this.y = a;
    }

    public int getX(){
        return this.x;
    }
    public int getY(){
        return this.y;
    }
}
